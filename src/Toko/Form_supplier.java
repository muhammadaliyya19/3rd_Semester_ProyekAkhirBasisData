    public void showData(){
        Object header[]={"Id_supplier","Nama","Id_produk","No_telepon","Alamat"};
        model = new DefaultTableModel(null , header);
        Tabel_supp.setModel(model);
        model.setRowCount(0);
        Koneksi.Konek();
        if (Koneksi.conn != null) {
            StatusKoneksi.setText("Koneksi Sukses");
        }else{
            StatusKoneksi.setText("Koneksi Gagal");
        }
        try {
        Statement stmt = Koneksi.conn.createStatement();
        String query = "SELECT * FROM Supplier";
        ResultSet rs = stmt.executeQuery(query);
        Object [] n = new Object[5];
            while (rs.next()) {
                n[0] = rs.getString("Id_supplier");
                n[1]  = rs.getString("Nama");
                n[2] = rs.getString("Id_produk");
                n[3]  = rs.getString("No_telepon");
                n[4]  = rs.getString("Alamat");
                model.addRow(n);
            }
        } catch (SQLException e ) {
            StatusKoneksi.setText("Gagal query");
        }
    }

