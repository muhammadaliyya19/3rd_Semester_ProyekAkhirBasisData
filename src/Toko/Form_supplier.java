    public void showSearchData(String kolom , String param){
        try {
            model.setRowCount(0);
            Statement stmt = Koneksi.conn.createStatement();
            String query = "SELECT * FROM Supplier WHERE "+ kolom+"='"+param+"';";
            ResultSet rs = stmt.executeQuery(query);
            Object [] n = new Object[5];
            while (rs.next()) {
                n[0] = rs.getString("Id_supplier");
                n[1]  = rs.getString("Nama");
                n[2] = rs.getString("Id_produk");
                n[3]  = rs.getString("No_telepon");
                n[4]  = rs.getString("Alamat");
                model.addRow(n);
            }        
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Data tidak ditemukan");
        }    
    }
